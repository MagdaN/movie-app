package com.example.magda.movieapp;


public class MovieDBTrailer {

    private final String mName;
    private final String mKey;

    public MovieDBTrailer(String name, String key) {
        mName = name;
        mKey = key;
    }

    public String getmName() {
        return mName;
    }

    public String getmKey() {
        return mKey;
    }


}
